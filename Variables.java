public class Variables {
    public static void main(String[] args) {
        int myInt = 702; // 32bit
        short myShort = 1702; // 16bit
        long myLong = 17020305; // 64bit

        double myDouble = 702.35; // 64bit
        float myFloat = 1702.35f; // 32bit

        char c = 'a';
        boolean b = true;

        byte myByte = 100; // 8bit -128-127
        System.out.println(myShort);
    }
}
